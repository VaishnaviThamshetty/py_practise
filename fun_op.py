num1 = 2
num2 = 6
def add(num1,num2):
    return num1 + num2
def sub(num1,num2):    
    return(num1 - num2)
def div(num1,num2):
    return(num1 / num2)
def mul(num1,num2):
    return(num1 * num2)
def mod(num1,num2):
    return(num1 % num2)
sum1 = add(num1,num2)
print('addition=',sum1)
sub1 = sub(num1,num2)
print('subtraction=',sub1)
mul1 = mul(num1,num2)
print('multiplication=',mul1)
div1 = div(num1,num2)
print('division=',div1)
mod1 = mod(num1,num2)
print('modulus=',mod1)
